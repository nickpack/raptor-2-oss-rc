void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
	if (htim->Instance == TIM2)
	{
		if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_2)  // if the interrupt source is channel2
			{
				if (CH2_is_First_Captured == 0) // if the first value is not captured
					{
						CH2_Val1 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_2);     // read the first value

						CH2_is_First_Captured = 1;       // set the first captured as true
						// Now change the polarity to falling edge
					
						__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_2, TIM_INPUTCHANNELPOLARITY_FALLING);
					}

				else if (CH2_is_First_Captured == 1)   // if the first is already captured
					{
						CH2_Val2 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_2);      // read second value
					
						__HAL_TIM_SET_COUNTER(htim, 0);     // reset the counter

						CH2_is_First_Captured = 0;     // set it back to false

						// set polarity to rising edge
						__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_2, TIM_INPUTCHANNELPOLARITY_RISING);

					}
			}
	
		if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_3)  // if the interrupt source is channel2
			{
				if (CH3_is_First_Captured == 0) // if the first value is not captured
					{
						CH3_Val1 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_3);      // read the first value
						CH3_is_First_Captured = 1;       // set the first captured as true
						// Now change the polarity to falling edge
					
						__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_3, TIM_INPUTCHANNELPOLARITY_FALLING);
					}

				else if (CH3_is_First_Captured == 1)   // if the first is already captured
					{
						CH3_Val2 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_3);       // read second value
						__HAL_TIM_SET_COUNTER(htim, 0);      // reset the counter

						CH3_is_First_Captured = 0;      // set it back to false

						// set polarity to rising edge
						__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_3, TIM_INPUTCHANNELPOLARITY_RISING);

					}
			
			}
	}
}



