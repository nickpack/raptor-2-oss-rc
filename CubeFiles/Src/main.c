/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_TIM1_Init();
  MX_TIM9_Init();
  /* USER CODE BEGIN 2 */
  enableMotors();
  enableServos(htim3, 1750);
  HAL_TIM_IC_Start_IT(&htim2, TIM_CHANNEL_2);
  HAL_TIM_IC_Start_IT(&htim2, TIM_CHANNEL_3);
  HAL_TIM_Base_Start_IT(&htim9);
  activateESCs(htim1);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	while (1)
	{
		if (!transmitter_calibrated)
		{
			calibrateTransmitter(CH2_Val2, CH3_Val2);
			
			if (calibrated_cnt == 6000)
			{
				transmitter_calibrated = 1;
				calibrated_cnt = 0;
				activateESCs(htim1);
			}
		}
		else if(transmitter_calibrated)
		{
			if (!communicationActive)
			{
				disableMotors();
				motorsDisabled = 1;
			}
			else if(communicationActive)
			{
				if(motorsDisabled)
				{
					enableMotors();
					motorsDisabled = 0;
				}
				
				angle = steerServos(htim3, CH2_Val2);
				
				// wheels
				THROTTLE_PWM = 1000 + (((CH3_Val2 - TRAN_Y_MIN) / (TRAN_Y_MAX - TRAN_Y_MIN)) * 1000);
				
				if (differentialActive == 1)
				{
					differential(angle, THROTTLE_PWM, THROTTLE_PWM, THROTTLE_PWM, THROTTLE_PWM);
				}
				else if(differentialActive == 0)
				{
					applyThrottle(htim1, THROTTLE_PWM, THROTTLE_PWM, THROTTLE_PWM, THROTTLE_PWM);
				}
			}
		}
	 
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	}
  /* USER CODE END 3 */
}

