//*******************************  1ms COUNTER  *******************************//
/////////////////////////////////////////////////////////////////////////////////
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	communicationCounter++;
	
	if (htim->Instance == TIM9 && transmitter_calibrated == 0)
	{
		calibrated_cnt += 1;
	}
	
	if (communicationCounter > 249)
	{
		// check to see if transmitter/reciever are still communicating
		communicationActive = communicationsCheck(CH2_Val1, CH2_Val2, CH3_Val1, CH3_Val2);
		communicationCounter = 0;
	}
}

